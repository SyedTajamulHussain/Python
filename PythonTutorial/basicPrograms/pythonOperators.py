# Arithmetic operators are used with numeric values to perform common mathematical operations
price = 25
GST = 3.4
Total = price + GST
print(Total)
print(Total - 2)
print(2 * 2)
print(4 / 2)
print(5 % 10)
print(4 // 2)

# Assignment operators are used to assign values to variables:
A = 5
print(A)
A += 5  # same as A = A + 3
print(A)
A -= 5  # same as A = A - 3
print(A)
A >>= 3
print(A)
A <<= 3
print(A)

